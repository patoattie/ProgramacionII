    @Override
    public void keyTyped(KeyEvent e)
    {
        switch(e.getKeyChar())
        {
            case KeyEvent.VK_ESCAPE:
                this.dialogoCancelado = true;
                this.setVisible(false);
                break;
            case KeyEvent.VK_ENTER:
                this.setVisible(false);
                break;
        }
    }

