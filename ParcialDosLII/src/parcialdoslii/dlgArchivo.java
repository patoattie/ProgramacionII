    public String getArchivoSeleccionado()
    {
        String retorno;
        
        if(this.cmbArchivos.getSelectedItem() == null)
        {
            retorno = "";
        }
        else
        {
            retorno = this.cmbArchivos.getSelectedItem().toString();
        }
        
        return retorno;
    }
    
    private void setComboEditable()
    {
        this.cmbArchivos.getEditor().getEditorComponent().addKeyListener(this);

        if(this.tipoDialogo.equals(TipoDialogoEnum.ABRIR))
        {
            this.cmbArchivos.setEditable(false);
        }
        else if(this.tipoDialogo.equals(TipoDialogoEnum.GUARDAR))
        {
            this.cmbArchivos.setEditable(true);
            this.cmbArchivos.setSelectedItem("");
        }
    }

